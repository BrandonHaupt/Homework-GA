// --------------------------------------
// DATA TYPES
// -------------------------------------

// NUMBER DATA TYPE (Primitive)
console.log(45)          // number
console.log("45")        // string
console.log(55 + 55)     // Will make it a number
console.log("55" + "55") // Will make it a string

// TYPE COERCION
console.log("55" + 55)  // using two different data types

// BOOLEAN (Primitive)
console.log(true, false) // Will output true and false
console.log(true + true) // true has a number value of 1 and false has it of 0

// OBJECTS (COLLECTION OF PROPERTIES) 
console.log({
    name: "Brandon", // key: value
    age: 30, // key: value
    email: "haupt.brandon@gmail.com", // key: value
    isGamer: true
})

// ARRAY (ORDER COLLECTION)
console.log(["Brandon", 30, "haupt.brandon@gmail.com", true])

// FUNCTION
function sayHello() {console.log("Hello there! My name is Brandon")} // Normal Function
let sayHi = () => {console.log(`Hello my name is Brandon nice to meet you`)} // Arrow Function



